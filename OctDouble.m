        function v = var( v, Flag, Dim )
            if nargin < 3
                Dim = [];
                if nargin < 2
                    Flag = 0;
                end
            end

            if isempty( Dim )
                Dim = find( size( v.v1 ) > 1, 1 );
                if isempty( Dim )
                    Dim = 1;
                end
            end

            Size = size( v.v1 );
            n = prod( Size( Dim ) );

            if ( n == 0 ) || ( ( n == 1 ) && ( Flag == 0 ) )
                Size( Dim ) = 1;
                v = OctDouble.Make( NaN( Size ), NaN( Size ) );
                return;
            end

            Mu = mean( v, Dim );
            v = v - Mu;
            v = v .* v;
            v = sum( v, Dim );

            if Flag == 1
                v = v ./ n;
            else
                v = v ./ ( n - 1 );
            end
        end

