        function y = linspace( a, b, n )
            if nargin < 3
                n = 100;
            end

            if ~isa( a, 'OctDouble' )
                a = OctDouble( a );
            end

            if ~isa( b, 'OctDouble' )
                b = OctDouble( b );
            end

            if n < 1
                y = OctDouble.zeros( 0, 1 );
                return;
            end

            if n == 1
                y = b;
                return;
            end

            Step = ( b - a ) ./ ( n - 1 );
            Indices = 0 : ( n - 1 );
            y = a + Step .* Indices;

            if n > 1
                y.v1( end ) = b.v1;
                y.v2( end ) = b.v2;
            end
        end

