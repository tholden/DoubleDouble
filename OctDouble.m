                for k = mn - 1 : -1 : 1
                    j = k + 1 : n;
                    [ t1, t2 ] = OctDouble.ODTimesOD( v.v1( j, : ), v.v2( j, : ), U.v1( k, j ).', U.v2( k, j ).' );
                    t = OctDouble.Sum( OctDouble.Make( t1, t2 ), 1 );
