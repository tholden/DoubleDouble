        function v = OctDouble( in, varargin )
            if nargin == 0
                v.v1 = QuadDouble( [] );
                v.v2 = QuadDouble( [] );
                return
            end
            if nargin >= 2
                for i = 1 : length( varargin )
                    in = OctDouble.Plus( in, varargin{ i } );
                end
            end
            if isa( in, 'OctDouble' )
                v.v1 = in.v1;
                v.v2 = in.v2;
            elseif isa( in, 'QuadDouble' )
                v.v1 = in;
                v.v2 = QuadDouble( zeros( size( in ) ) );
            elseif isa( in, 'DoubleDouble' )
                v.v1 = QuadDouble( in );
                v.v2 = QuadDouble( zeros( size( in ) ) );
            else
                v.v1 = QuadDouble( in );
                v.v2 = QuadDouble( zeros( size( in ) ) );
            end
        end

